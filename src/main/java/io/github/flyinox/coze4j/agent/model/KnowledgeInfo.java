package io.github.flyinox.coze4j.agent.model;

/**
 * Knowledge base information
 */
public class KnowledgeInfo {
    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
